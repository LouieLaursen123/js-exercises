/**
 * Function that adds up numbers from 1
 * @returns The overall sum of the value from startNum from number 1
 */

const drinks = [
  { name: 'lemonade', price: 50 },
  { name: 'lime', price: 10 },
  { name: 'cola', price: 25 },
]

export const sortDrinksByPrice = (): Number => {
  return drinks.sort()
}
