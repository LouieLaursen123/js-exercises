/**
 * Function that adds up numbers from 1
 * @param startNum - Value of the number, which adds up from number 1 and calculates the sum
 * @returns The overall sum of the value from startNum from number 1
 */

export const addupNumbers = (startNum: number): number => {
  return (startNum = (startNum * (startNum + 1)) / 2)
}
